    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'word_puzzle_id' => 'required', 
            'clue'          => 'required',
            'answer'        => 'required',
            'orrientation'  => 'required'
        ]);

        $word = new PuzzleWord;
        $word->word_puzzle_id = $request->word_puzzle_id;
        $word->clue = $request->clue;
        $word->answer = $request->answer;
        $word->orrientation = $request->orrientation;
        $word->save();

        return back();
    }

